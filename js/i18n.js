window.currentLang = localStorage.getItem('lang') || 'sv';

window.setLang = function(lang) {
    window.currentLang = lang;
    localStorage.setItem('lang', lang);
    window.renderI18n();
    if(typeof renderAll === 'function') renderAll();
    if(typeof renderGantt === 'function') renderGantt();
};

window.t = function(keyStr) {
    if(!keyStr) return "";
    let safeKey = keyStr.toLowerCase().replace(/ /g, '_').replace(/\//g, '_');
    const dictionary = translations[window.currentLang] || translations['sv'];
    return dictionary[safeKey] || translations['sv'][safeKey] || keyStr;
};

window.renderI18n = function() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        
        let translated = window.t(key);
        
        if (el.tagName.toLowerCase() === 'input' && el.type === 'button') {
            el.value = translated;
        } else {
            // Check if element has a phosphor icon
            const icon = el.querySelector('i');
            if(icon) {
                el.innerHTML = '';
                el.appendChild(icon);
                
                // Keep styling span if present
                const span = document.createElement('span');
                span.textContent = translated;
                el.appendChild(span);
            } else {
                el.textContent = translated;
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = window.t(key);
    });
};

document.addEventListener('DOMContentLoaded', () => {
    window.renderI18n();
});
